var animals = ["dog", "cat", "chick"];
//printing names using for loop
for (var i = 0; i < animals.length; i++) {
    console.log(animals[i]);
    //modifying the loop by printing the statement
    console.log("A", animals[i], "would make a great pet.\n");
}
console.log("\nAll of the above animals", animals, "are pet animals.");
